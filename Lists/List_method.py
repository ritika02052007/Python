# list methods

marks = [56,78,89,90,92,95]
print(marks.append(88))   # give the none value but it insert the value
marks.append(83)
print(marks)

print("--Use the sort method--")
marks.sort()
print(marks)

print("--Use the reverse method--")
marks.reverse()
print(marks)

print("--Use the insert method--")
marks.insert(4,34)
print(marks)

print("--Use the remove method--")
marks.remove(34)
print(marks)

print("--Use the pop method--")
marks.pop(3)
print(marks)

