# basic of for loop

list1 = [2,4,6,8,10]
for el in list1 :
    print(el)

list2 = ["apple" ,"banana","litchi" ,"grapes"]
for el in list2 :
    print(el)


# use tuple in for loop

tuple1 = ("hello" ,"world" ,"apna" ,"college")
for el in tuple1 :
    print(el)


# print string using for loop
str = "apna colllege"
for el in str :
    print(el)